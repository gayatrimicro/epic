        <h2>Causes of Cardiovascular Disease</h2>

        
  <div class="general-content">

    
      
        <p class="p1">Coronary Artery Disease is caused due to fatty deposits in the arteries. This process of accumulation is called Atherosclerosis. It’s a condition when arteries become narrowed by a gradual build-up of fatty material called atheroma within their walls.  This fatty deposits cause narrowing and restrict the blood flow through the artery. As time goes by, arteries get thinner that they cannot deliver enough oxygen-rich blood to the heart. This causes angina – a pain or chest discomfort.</p>

        <p class="p1">Atherosclerosis also referred to as hardening or clogging of the arteries is the build-up of cholesterol and fatty deposits known as plaques. These plaques prevent the supply of blood to the heart muscle by physically blocking the artery or by causing abnormal artery tone and function. If the blood supply to a portion of the heart muscle is restrained, or when the energy demand of the heart is higher than its blood supply, a person faces heart attack/Myocardial infarction. </p>

        <p class="p1">
          When a piece of atheroma in the arteries breaks away, it forms a blood clot. This blood clot blocks the coronary artery and cuts off the supply of oxygen-rich blood to the heart muscle, the heart muscle may become permanently damaged. In medical terms, we call it a heart attack. When a blood clot blocks an artery that carries blood to the brain, it can cut off the blood supply to part of your brain causing a stroke.

        </p>

