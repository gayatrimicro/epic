<li>Damage to joints such as ankles, knees, and elbows</li>
<li>The development of inhibitors (which interfere with factor replacement therapy)</li>
<li>Infections from CVD (central venous devices)</li>
<li>Neurological conditions caused by intracranial bleeding events</li>
<li>Pseudotumors (a rare complication which occurs in 1% - 2% of hemophilia patients)</li>
<li>Compartment syndrome which can cause dangerously high pressure within muscle compartments and can lead to the death of the affected tissues</li>

